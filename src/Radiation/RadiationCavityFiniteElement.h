#ifndef __fesystem_radiation_cavity_finite_element_h__
#define __fesystem_radiation_cavity_finite_element_h__


// C++ includes


// FESystem includes




/// this defines a finite element that participates in the
/// radiation cavity analysis
struct RadiationCavityFiniteElem
{
  /// ID of the finite element
  unsigned int elem_ID;

  /// side number of the element that participates in the element
  int side_num;

  /// number of radiation elem divisions in the x-direction of the element
  unsigned int n_x_divs;

  /// number of radiation elem divisions in the y-direction of the element
  unsigned int n_y_divs;

  /// internal ID of the elem
  unsigned int internal_ID;
};




#endif // __fesystem_radiation_cavity_finite_element_h__
